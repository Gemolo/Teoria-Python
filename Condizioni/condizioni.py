# Le condizioni servono per verificare una variabile o più in base ad un criterio
# Viene utilizzato la dicitura if ad esempio:

numero = 50

# Questo codice dirà se il numero è maggiore di 50

if numero < 50:
    print("numero < 50")
else:
    print("Numero >= 50")

# Questo codice dice se un numero è pari o no

if numero % 2 == 0:
    print("Numero pari")
else:
    print("Numero dispari")

# Questo codice dice se una stringa ha lunghezza maggiore di 5

parola = "ciao"
if len(parola) > 5:
    print("Stringa con lunghezza maggiore di 5")
else:
    print("Stringa con lunghezza inferiore di 5")

